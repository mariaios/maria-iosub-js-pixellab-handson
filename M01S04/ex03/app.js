var errorCode = 300;

switch (errorCode) {
  case 100:
    console.log('Error 100');
    break;
  case 200:
    console.log('Error 200');
    break;
  case 300:
    console.log('Error 300');

  case 400:
    console.log('Error 400');
    break;
  case 500:
    console.log('Error 500');
    break;
  default:
    console.log('No error');
}
